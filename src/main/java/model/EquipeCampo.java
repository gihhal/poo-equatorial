package model;

public class EquipeCampo {
    private String id;
    private String nomeEquipe;
    private String baseOperacional;

    public String getId() { return id; }
    public void setId(String id) { this.id = id; }

    public String getNomeEquipe() { return nomeEquipe; }
    public void setNomeEquipe(String nomeEquipe) { this.nomeEquipe = nomeEquipe; }

    public String getBaseOperacional() { return baseOperacional; }
    public void setBaseOperacional(String baseOperacional) { this.baseOperacional = baseOperacional; }
}
